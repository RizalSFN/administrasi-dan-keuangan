    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String nama = txtNama.getText();
        String deskripsi = txtDeskripsi.getText();

        if (nama.isEmpty() || deskripsi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Field tidak boleh kosong!");
            return;
        }

        IncomeSource is = new IncomeSource();
        is.setNama(nama);
        is.setDeskripsi(deskripsi);

        IncomeSourceController isc = new IncomeSourceController();
        boolean success = isc.createIncomeSource(is);

        if (success) {
            JOptionPane.showMessageDialog(this, "Kategori berhasil ditambahkan!!");
            tampilDataKategori();
            txtNama.setText("");
            txtDeskripsi.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambah kategori!");
        }

    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int selectedRow = tabelKategori.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang mau diubah dulu!");
            return;
        }

        int id = (int) tabelKategori.getValueAt(selectedRow, 0);
        String newNama = txtNama.getText().trim();
        String newDeskripsi = txtDeskripsi.getText().trim();

        if (newNama.isEmpty() || newDeskripsi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Field tidak boleh kosong!");
            return;
        }

        IncomeSource ic = new IncomeSource();
        ic.setId(id);
        ic.setNama(newNama);
        ic.setDeskripsi(newDeskripsi);

        IncomeSourceController controller = new IncomeSourceController();
        boolean success = controller.updateIncomeSource(ic);

        if (success) {
            JOptionPane.showMessageDialog(this, "Kategori berhasil diubah!");
            tampilDataKategori();
            txtNama.setText("");
            txtDeskripsi.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal mengubah kategori!");
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void tabelKategoriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKategoriMouseClicked
        int selectedRow = tabelKategori.getSelectedRow();
        if (selectedRow != -1) {
            txtNama.setText((String) tabelKategori.getValueAt(selectedRow, 1));
            txtDeskripsi.setText((String) tabelKategori.getValueAt(selectedRow, 2));
        }
    }//GEN-LAST:event_tabelKategoriMouseClicked


