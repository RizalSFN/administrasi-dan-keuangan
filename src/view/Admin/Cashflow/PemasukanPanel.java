    private void tampilDataKategori() {
        IncomeSourceController controller = new IncomeSourceController();
        List<IncomeSource> kategoriList = controller.getAllKategoriPemasukan();

        // Siapkan kolom
        String[] kolom = {"ID", "Nama", "Status"};
        DefaultTableModel model = new DefaultTableModel(null, kolom);

        // Tambahkan baris
        for (IncomeSource ic : kategoriList) {
            Object[] row = {
                ic.getId(),
                ic.getNama(),
                ic.getDeskripsi(),
                ic.getStatus()
            };
            model.addRow(row);
        }

        tabelKategori.setModel(model);
    }

