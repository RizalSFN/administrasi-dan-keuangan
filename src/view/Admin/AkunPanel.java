    private void tampilkanUserKeTabel() {
        int nomor = 1;

        try {
            Connection conn = DatabaseConnection.getConnection();
            UserController controller = new UserController(conn);
            List<User> users = controller.getAllUsers();

            String[] kolom = {"No", "ID", "Username", "Email", "Nomor Telepon", "Status"};
            DefaultTableModel model = new DefaultTableModel(null, kolom);

            for (User user : users) {
                Object[] row = {
                    nomor++,
                    user.getId(),
                    user.getUsername(),
                    user.getEmail(),
                    user.getNomorTelepon(),
                    user.getStatus()
                };
                model.addRow(row);
            }

            tabelAkun.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

