    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String email = txtEmail.getText();
        String telepon = txtTelepon.getText();

        User user = new User();
        user.setUsername(username);
        user.setPassword(password); // akan di-hash di DAO
        user.setRole("siswa");
        user.setEmail(email);
        user.setNomorTelepon(telepon);

        try {
            Connection conn = DatabaseConnection.getConnection(); // asumsi kamu punya ini
            UserController controller = new UserController(conn);
            boolean success = controller.createUser(user);

            if (success) {
                JOptionPane.showMessageDialog(this, "User berhasil ditambahkan!");
                tampilkanUserKeTabel(); // refresh data
                txtUsername.setText("");
                txtPassword.setText("");
                txtEmail.setText("");
                txtTelepon.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan user.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnSiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiswaActionPerformed
        int selectedRow = tabelAkun.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih salah satu akun terlebih dahulu!");
            return;
        }

        // Ambil user_id dari tabel
        int userId = (int) tabelAkun.getValueAt(selectedRow, 1); // sesuaikan indeks kolom user_id

        // Panggil panel Siswa dengan userId yang dipilih
        adminDashboard.showStudentPanel(userId);
    }//GEN-LAST:event_btnSiswaActionPerformed


